/// Список цветов
public enum ColorToken: String {
  
  /// Основной черный цвет
  case primaryBlack = "313131"
  
  /// Темно-серый цвет
  case primaryGray = "96959B"
  
  /// Светло-серый цвет
  case secondaryGray = "F3F3F5"
  
  /// Светло-белый цвет
  case primaryWhite = "FFFFFF"
}
